    public static void SimulateBattle(List<Alien> teamA, List<Alien> teamB)
    {
        int indexA = 0, indexB = 0;

        while (indexA < teamA.Count && indexB < teamB.Count)
        {
            var alienA = teamA[indexA];
            var alienB = teamB[indexB];

            Console.WriteLine($"FIGHT: A{indexA + 1} (Speed: {alienA.Speed}, Strength: {alienA.Strength}, Health: {alienA.Health}) " +
                              $"vs. B{indexB + 1} (Speed: {alienB.Speed}, Strength: {alienB.Strength}, Health: {alienB.Health})");

            while (alienA.Health > 0 && alienB.Health > 0)
            {
                if (alienA.Speed > alienB.Speed || (alienA.Speed == alienB.Speed))
                {
                    // Team A attacks
                    alienB.Health -= alienA.Strength;
                    if (alienB.Health <= 0)
                    {
                        Console.WriteLine($"B{indexB + 1} is defeated.");
                        indexB++;
                        break;
                    }

                    // Team B attacks
                    alienA.Health -= alienB.Strength;
                    if (alienA.Health <= 0)
                    {
                        Console.WriteLine($"A{indexA + 1} is defeated.");
                        indexA++;
                        break;
                    }
                }
                else
                {
                    // Team B attacks first
                    alienA.Health -= alienB.Strength;
                    if (alienA.Health <= 0)
                    {
                        Console.WriteLine($"A{indexA + 1} is defeated.");
                        indexA++;
                        break;
                    }

                    // Team A attacks
                    alienB.Health -= alienA.Strength;
                    if (alienB.Health <= 0)
                    {
                        Console.WriteLine($"B{indexB + 1} is defeated.");
                        indexB++;
                        break;
                    }
                }
            }
        }

        // Determine winner
        if (indexA == teamA.Count)
        {
            int remainingHealthB = CalculateRemainingHealth(teamB, indexB);
            Console.WriteLine($"Team B wins with {remainingHealthB} health remaining.");
        }
        else
        {
            int remainingHealthA = CalculateRemainingHealth(teamA, indexA);
            Console.WriteLine($"Team A wins with {remainingHealthA} health remaining.");
        }
    }

    public static int CalculateRemainingHealth(List<Alien> team, int startIndex)
    {
        int totalHealth = 0;
        for (int i = startIndex; i < team.Count; i++)
        {
            totalHealth += Math.Max(0, team[i].Health);
        }
        return totalHealth;
    }
